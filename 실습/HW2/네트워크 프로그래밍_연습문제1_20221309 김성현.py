a = "My name is SungHyun kim"
print(len(a))  # 문자열의 문자 수를 출력하라
print(a*10)    # 문자열을 10번 반복한 문자열을 출력하라
print(a[0])    # 문자열에서 첫 번째 문자를 출력하라
print(a[0:5])  # 문자열에서 처음 4문자를 출력하라
print(a[-4:])  # 문자열에서 마지막 4문자를 출력하라
print(a[-1:])  # 문자열에서 문자를 거꾸로 출력하라 
print(a[1:-1]) # 문자열에서 첫 번째 문자와 마지막 문자를 제거한 문자열을 출력하라
print(a.upper())# 문자를 모두 대문자로 변경하여 출력하라
print(a.lower())# 문자를 모두 소문자로 변경하여 출력하라
print(a.replace('a', 'e'))# 문자열에서 'a'를 'e'로 대체하여 출력하라라